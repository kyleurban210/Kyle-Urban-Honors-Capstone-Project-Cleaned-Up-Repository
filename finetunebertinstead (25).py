def compute_metrics(eval_pred):
  #https://datascience.stackexchange.com/questions/31041/what-does-logits-in-machine-learning-mean
  #logits are like the last scores of the final layer of a model that decide the confidence of whether something is in one cateory or the other
  logits, labels  = eval_pred #unpack the tuple
  predictions = np.argmax(logits, axis=-1)  #this finds the most confident logit (highest/best prediction) of the last layer
  #the axis = -1 makes the damn thing look at the last layer https://stackoverflow.com/questions/73794766/what-is-the-meaning-of-axis-1-in-tf-keras-layers-normalization
  return {"accuracy": accuracy_score(labels, predictions)} #should give a column on data table (ngl I did steal this function from gemini)


