def trial_maker(unmod_data, mod_data, trials_do, num_train_epochs, logging_steps, num_layers_to_freeze,
                per_device_train_batch_size, per_device_eval_batch_size, skip_small, fp16, weight_decay, file_name):
  print("multiple_trials")
  #since the for loop in this function creates 10 models, I am going to need to clear up memory.
  total_results_mod = []
  tally_accu_mod = 0
  tally_loss_mod = 0

  total_results_unmod = []
  tally_accu_unmod = 0
  tally_loss_unmod = 0

  #remember, training is only done on the unmod
  total_results_train = []
  tally_accu_train = 0
  tally_loss_train = 0


  all_seeds = []


  for num_trials in range(1, trials_do + 1):
    # --- CRITICAL FIX: RESET SEEDS ---  added via gemini 2/4/26
    # Using a different seed for every trial ensures different initialization and shuffling
    current_seed = 42 + num_trials
    random.seed(current_seed)
    np.random.seed(current_seed)
    torch.manual_seed(current_seed)
    torch.cuda.manual_seed_all(current_seed)
    set_seed(current_seed) # Hugging Face specific seed setter

    all_seeds.append(current_seed)

    print(f"\n--- Starting Trial {num_trials} with Seed {current_seed} ---")

    #trains model on unmod and does its eval on unmod
    trainer, results_unmod, results_train, small_data_set_size, train_indices = train_model(unmod_data, num_train_epochs, logging_steps,
                                         num_layers_to_freeze, per_device_train_batch_size,
                                         per_device_eval_batch_size, skip_small, fp16, current_seed, weight_decay)
    total_results_unmod.append(results_unmod)
    tally_loss_unmod += results_unmod['eval_loss']
    tally_accu_unmod += results_unmod['eval_accuracy']

    #fyi train_indices are the indices of sentence pairs in the unmod that were used to train the model.
    #they must be used to purge those sentence pairs from the eval of the mod (to prevent data leakage!)

    total_results_train.append(results_train)
    tally_loss_train += results_train.metrics['train_loss']

    #evaluates on mod data
    results_mod = experiment(mod_data, trainer, train_indices)
    total_results_mod.append(results_mod)
    tally_loss_mod += results_mod['eval_loss']
    tally_accu_mod += results_mod['eval_accuracy']


    #save partial data incase of a crash

    loss_avg_mod_part = tally_loss_mod/num_trials
    accu_avg_mod_part= tally_accu_mod/num_trials

    loss_avg_unmod_part = tally_loss_unmod/num_trials
    accu_avg_unmod_part = tally_accu_unmod/num_trials

    loss_avg_train_part = tally_loss_train/num_trials

    num_trials_s = str(num_trials)
    trials_do_s = str(trials_do)

    part_name = "completed_" +  num_trials_s + "trials out of_" + trials_do_s + "____"

    results_file(num_trials, loss_avg_unmod_part, accu_avg_unmod_part, total_results_unmod, loss_avg_mod_part,
               accu_avg_mod_part, total_results_mod, num_train_epochs, logging_steps, num_layers_to_freeze,
               per_device_train_batch_size, per_device_eval_batch_size, skip_small, part_name,
               loss_avg_train_part, total_results_train, fp16, small_data_set_size, all_seeds)



    print(f"cleaning memory for trial {num_trials}")
    trainer.model.cpu()
    del trainer
    gc.collect()
    torch.cuda.synchronize() #add some waiting
    torch.cuda.empty_cache()
    print(f"Memory cleaned. GPU allocated: {torch.cuda.memory_allocated()}")


  loss_avg_mod = tally_loss_mod/trials_do
  accu_avg_mod = tally_accu_mod/trials_do

  loss_avg_unmod = tally_loss_unmod/trials_do
  accu_avg_unmod = tally_accu_unmod/trials_do

  loss_avg_train = tally_loss_train/trials_do

  results_file(trials_do, loss_avg_unmod, accu_avg_unmod, total_results_unmod, loss_avg_mod,
               accu_avg_mod, total_results_mod, num_train_epochs, logging_steps, num_layers_to_freeze,
               per_device_train_batch_size, per_device_eval_batch_size, skip_small, file_name,
               loss_avg_train, total_results_train, fp16, small_data_set_size, all_seeds)

  results_file_pdf(trials_do, loss_avg_unmod, accu_avg_unmod, total_results_unmod, loss_avg_mod,
               accu_avg_mod, total_results_mod, num_train_epochs, logging_steps, num_layers_to_freeze,
               per_device_train_batch_size, per_device_eval_batch_size, skip_small, file_name,
               loss_avg_train, total_results_train, fp16, small_data_set_size, all_seeds, weight_decay)

  return 1

